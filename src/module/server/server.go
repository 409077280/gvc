package server

import (
	"github.com/gin-gonic/gin"
	"os"
	"os/signal"
	"time"
	"fmt"
	"io/ioutil"
	"log"
	"net/http"
	"config"
	"context"
)

func Run(router *gin.Engine) {

	http.Handle("/", http.FileServer(http.Dir("views")))

	srv := &http.Server{
		Addr:    ":" + config.GetEnv().SERVER_PORT,
		Handler: router,
	}

	go func() {
		// service connections
		if err := srv.ListenAndServeTLS("./ssl/cert.pem", "./ssl/key.pem"); err != nil && err != http.ErrServerClosed {
			log.Fatalf("listen: %s\n", err)
		}
	}()

	// Wait for interrupt signal to gracefully shutdown the server with
	// a timeout of 10 seconds.
	quit := make(chan os.Signal)
	signal.Notify(quit, os.Interrupt)
	<-quit
	log.Println("Shutdown Server ...")

	ctx, cancel := context.WithTimeout(context.Background(), 10*time.Second)
	defer cancel()
	if err := srv.Shutdown(ctx); err != nil {
		log.Fatal("Server Shutdown:", err)
	}
	log.Println("Server exiting")

	pid := fmt.Sprintf("%d", os.Getpid())
	_, openErr := os.OpenFile("pid", os.O_RDWR|os.O_CREATE|os.O_APPEND, 0666)
	if openErr == nil {
		ioutil.WriteFile("pid", []byte(pid), 0)
	}
}